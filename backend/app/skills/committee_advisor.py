    def _overview(self) -> ChatResponse:
        docs = COMMITTEE_MATERIALS["documents"]
        doc_list = "\n".join([f"- **{d['title']}** ({d['type']}, {d['pages']} стр.)" for d in docs])
        contra_count = len(COMMITTEE_MATERIALS["contradictions"])
        risk_count = len(COMMITTEE_MATERIALS["risks"])
        return ChatResponse(session_id="", blocks=[
            ContentBlock(type="text", data=(
                "## Обзор материалов инвестиционного комитета\n\n"
                f"Загружено **{len(docs)} документа** общим объемом {sum(d['pages'] for d in docs)} страниц:\n\n"
                f"{doc_list}\n\n"
                f"### Предварительный анализ\n"
                f"- Выявлено **{contra_count} противоречия** между документами\n"
                f"- Идентифицировано **{risk_count} рисков** различной степени критичности\n\n"
                "Задайте уточняющий вопрос: противоречия, риски, рекомендация, "
                "чеклист DD, вопросы менеджменту, скоркард или драфт протокола."
            )),
        ])

    def _find_contradictions(self) -> ChatResponse:
        contras = COMMITTEE_MATERIALS["contradictions"]
        rows = []
        for c in contras:
            row = [
                c["parameter"],
                f"{c['doc1']}: {c['doc1_value']} ({c['doc1_page']})",
                f"{c['doc2']}: {c['doc2_value']} ({c['doc2_page']})",
                f"{c['doc3']}: {c['doc3_value']} ({c['doc3_page']})" if c.get("doc3") else "—",
            ]
            rows.append(row)

        text_parts = ["## Выявленные противоречия между документами\n"]
        for i, c in enumerate(contras, 1):
            sev = "[КРИТ]" if c["severity"] == "high" else "[ВЫСОК]"
            text_parts.append(f"### {sev} Противоречие {i}: {c['parameter']}\n")
            text_parts.append(f"- **{c['doc1']}** ({c['doc1_page']}): {c['doc1_value']}")
            text_parts.append(f"- **{c['doc2']}** ({c['doc2_page']}): {c['doc2_value']}")
            if c.get("doc3"):
                text_parts.append(f"- **{c['doc3']}** ({c['doc3_page']}): {c['doc3_value']}")
            text_parts.append(f"\n*{c['comment']}*\n")

        return ChatResponse(session_id="", blocks=[
            ContentBlock(type="text", data="\n".join(text_parts)),
            ContentBlock(type="table", data={
                "headers": ["Параметр", "Документ 1", "Документ 2", "Документ 3"],
                "rows": rows,
                "caption": "Сводка противоречий между документами",
            }),
        ])

    def _risk_analysis(self) -> ChatResponse:
        risks = COMMITTEE_MATERIALS["risks"]
        sev_map = {"high": "[КРИТ] Высокая", "medium": "[СРЕДН] Средняя", "low": "[НИЗК] Низкая"}
        prob_map = {"high": "60-80%", "medium": "30-50%", "low": "10-20%"}
        sev_sort = {"high": 0, "medium": 1, "low": 2}
        sorted_risks = sorted(risks, key=lambda r: (sev_sort[r["severity"]], sev_sort[r["probability"]]))

        rows = [[r["name"], sev_map[r["severity"]], prob_map[r["probability"]], r["impact"], r["source"]] for r in sorted_risks]

        # Group by category
        categories = {
            "Финансовые": ["risk_1", "risk_2", "risk_3", "risk_10"],
            "Рыночные": ["risk_4"],
            "Строительные": ["risk_6", "risk_7", "risk_9"],
            "Юридические и регуляторные": ["risk_5", "risk_8"],
        }
        risk_by_id = {r["id"]: r for r in sorted_risks}

        text = "## Матрица рисков сделки: Логистический хаб Подмосковье\n\n"
        text += f"Выявлено **{len(risks)} рисков** в 4 категориях.\n\n"

        for cat_name, cat_ids in categories.items():
            cat_risks = [risk_by_id[rid] for rid in cat_ids if rid in risk_by_id]
            if not cat_risks:
                continue
            text += f"### {cat_name}\n\n"
            for r in cat_risks:
                marker = sev_map[r["severity"]].split()[0]
                text += f"**{marker} {r['name']}**\n"
                text += f"- Влияние: {r['impact']}\n"
                text += f"- Вероятность: {prob_map[r['probability']]}\n"
                text += f"- Источник: {r['source']}\n\n"

        high_count = sum(1 for r in risks if r["severity"] == "high")
        text += f"---\n**Итого:** {high_count} критических, {sum(1 for r in risks if r['severity'] == 'medium')} средних, {sum(1 for r in risks if r['severity'] == 'low')} низких рисков.\n"
        text += "*Рекомендация: устранить критические риски (заполняемость, стройматериалы, ставка ЦБ) до одобрения сделки.*"

        return ChatResponse(session_id="", blocks=[
            ContentBlock(type="text", data=text),
            ContentBlock(type="table", data={
                "headers": ["Риск", "Критичность", "Вероятность", "Влияние на NPV/IRR", "Источник"],
                "rows": rows,
                "caption": "Полная матрица рисков инвестиционного проекта",
            }),
        ])

