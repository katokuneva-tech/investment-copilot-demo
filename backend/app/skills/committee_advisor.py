class CommitteeAdvisorSkill(BaseSkill):
    async def handle(self, message: str, session_id: str, extra_context: str = "") -> ChatResponse:
        msg = message.lower()
        # Portfolio-mode: АФК-specific checks first (more specific → before generic)
        if any(k in msg for k in ["допэмисс", "логику допэм", "логика допэм"]):
            return self._etalon_check()
        elif any(k in msg for k in ["нагрузк", "пик погашени", "рейтинг афк", "долг афк", "обязательств афк"]):
            return self._afk_debt_risk()
        elif any(k in msg for k in ["pre-analysis", "pre analysis", "инвентариз", "brief"]):
            return self._pre_analysis_brief()
        # Deal-mode: document analysis
        elif any(k in msg for k in ["противоречи", "расхожден", "несоответств"]):
            return self._find_contradictions()
        elif any(k in msg for k in ["чеклист", "due diligence", "дью", "чего не хватает"]):
            return self._dd_checklist()
        elif any(k in msg for k in ["вопрос", "спросить", "задать", "менеджмент"]):
            return self._management_questions()
        elif any(k in msg for k in ["скоркард", "scorecard", "взвешен оценк"]):
            return self._scorecard()
        elif any(k in msg for k in ["протокол", "драфт", "решение комитет"]):
            return self._protocol_draft()
        elif any(k in msg for k in ["рекоменд", "стоит ли", "входить", "за и против"]):
            return self._recommendation()
        elif any(k in msg for k in ["риск", "угроз", "красн", "флаг", "segezha", "сегеж"]):
            return self._risk_analysis()
        else:
            return self._overview()

